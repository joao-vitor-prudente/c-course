#include <stdio.h>
#include <stdbool.h>

int main(int argc, char const *argv[])
{
    char a = 'a';
    char b[] = "Hello World";  // array of characters
    float c = 3.14; // 32 bit
    double d = 3.14159265358979323846;  // 64 bit  %lf
    bool e = true;
    unsigned int f = 10;  // %u
    long long int g = 1000000000000000000U;  // 64 bit  %lld U = unsigned
    short int h = 1000;  // 16 bit  %hd
    return 0;
}
